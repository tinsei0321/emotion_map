# ════════════ 查询函数 ═════════════

def landuse_name(code):
    """任意位数代码（2/4/6）→ 名称。未命中返回 None。"""
    code = str(code).strip()
    if len(code) == 2:
        return LANDUSE_L1.get(code)
    if len(code) == 4:
        return LANDUSE_L2.get(code)
    if len(code) == 6:
        return LANDUSE_L3.get(code)
    return None

def landuse_level(code):
    """代码 → 层级（1/2/3）。未命中返回 0。"""
    code = str(code).strip()
    if code in LANDUSE_L1:
        return 1
    if code in LANDUSE_L2:
        return 2
    if code in LANDUSE_L3:
        return 3
    return 0

def landuse_parent(code):
    """代码 → 父级代码。二级→一级，三级→二级，一级→None。"""
    code = str(code).strip()
    if len(code) == 4:
        return code[:2]
    if len(code) == 6:
        return code[:4]
    return None

def landuse_children(code):
    """代码 → 下级代码列表。一级→其二级，二级→其三级，三级→[]。"""
    code = str(code).strip()
    if len(code) == 2:
        return [c for c in LANDUSE_L2 if c.startswith(code)]
    if len(code) == 4:
        return [c for c in LANDUSE_L3 if c.startswith(code)]
    return []

def landuse_search(keyword):
    """名称模糊查代码（含关键字的所有层类）。返回 [(code, name, level), ...]。"""
    kw = str(keyword)
    out = []
    for c, n in LANDUSE_L1.items():
        if kw in n:
            out.append((c, n, 1))
    for c, n in LANDUSE_L2.items():
        if kw in n:
            out.append((c, n, 2))
    for c, n in LANDUSE_L3.items():
        if kw in n:
            out.append((c, n, 3))
    return out


